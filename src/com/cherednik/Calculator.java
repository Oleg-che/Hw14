package com.cherednik;

public class Calculator {

    public int plus(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b ;
    }

    public double multiply(int a, int b) {
        return (double) a * b;
    }

    public double division(int a, int b) {
        return (double) a / b;
    }
}
